namespace ComputeCache
{
    /// <summary>
    /// 一份内存内固定存在的 16 个 bank：标量 (S) 与向量 (V) 各 8 个能级。
    /// 低 3 位即能级序号（0..7 对应 1/4/8/16/32/64/128/256 字节），第 4 位区分标量(0)/向量(1)。
    /// </summary>
    public enum BankKind : byte
    {
        S1 = 0, S4 = 1, S8 = 2, S16 = 3, S32 = 4, S64 = 5, S128 = 6, S256 = 7,
        V1 = 8, V4 = 9, V8 = 10, V16 = 11, V32 = 12, V64 = 13, V128 = 14, V256 = 15,
    }
}
